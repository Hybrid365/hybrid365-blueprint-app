import type { HyroxSessionCategory, HyroxSessionDefinition, HyroxSessionVariant } from "./types";
import { enrichHyroxSessionLibrary } from "./sessionLibraryEnrichments";

function v(
  summary: string,
  extra?: Partial<Omit<HyroxSessionVariant, "summary">>
): HyroxSessionVariant {
  return { summary, ...extra };
}

function defineSession(
  partial: Omit<HyroxSessionDefinition, "difficultyLevels"> & {
    difficultyLevels?: HyroxSessionDefinition["difficultyLevels"];
  }
): HyroxSessionDefinition {
  return {
    difficultyLevels: ["beginner", "intermediate", "advanced", "pro"],
    ...partial,
  };
}

