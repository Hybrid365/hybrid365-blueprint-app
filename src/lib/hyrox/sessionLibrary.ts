/** Base templates + enrichments (progression examples, film prompts, prescription rationale). */
export const HYROX_SESSION_LIBRARY: HyroxSessionDefinition[] =
  enrichHyroxSessionLibrary(HYROX_SESSION_LIBRARY_BASE);

export const HYROX_SESSION_LIBRARY_BASE_EXPORT = HYROX_SESSION_LIBRARY_BASE;

export const HYROX_SESSION_BY_ID: Record<string, HyroxSessionDefinition> = Object.fromEntries(
  HYROX_SESSION_LIBRARY.map((s) => [s.id, s])
);

export function getHyroxSession(id: string): HyroxSessionDefinition | undefined {
  return HYROX_SESSION_BY_ID[id];
}

export function getHyroxSessionsByCategory(
  category: HyroxSessionCategory
): HyroxSessionDefinition[] {
  return HYROX_SESSION_LIBRARY.filter((s) => s.category === category);
}

export function searchHyroxSessionsByTag(tag: string): HyroxSessionDefinition[] {
  const t = tag.toLowerCase();
  return HYROX_SESSION_LIBRARY.filter((s) => s.tags.some((x) => x.toLowerCase().includes(t)));
}
