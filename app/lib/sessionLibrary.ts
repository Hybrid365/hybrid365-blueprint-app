export type TimeRequirement = "30-45" | "45-75" | "75+";
export type Intensity = "low" | "medium" | "high";
export type Fatigue = "low" | "medium" | "high";

export type SessionBlock = {
  warm_up?: string[];
  main: string[];
  cool_down?: string[];
  finish?: string[];
  notes?: string[];
};

export type SessionTemplate = {
  id: string;
  name: string;
  type: SessionType;
  category: SessionCategory;

  intensity: Intensity;
  fatigue: Fatigue;
  duration: number;

  goal: GoalFocus[];
  experience: AbilityLevel[];

  equipment: UserEquipment[];
  time_requirement: TimeRequirement;

  avoid_after: string[];
  compatible_with: string[];

  variation_group: string;
  structure_roles: StructureRole[];

  coaching: {
    intent: string;
    cue: string;
    rpe: string;
  };

  prescription: SessionBlock;
};

export type DayPlan = {
  day: DayKey;
  title: string;
  intent?: string;
  session: SessionBlock;
  time_cap_minutes?: number;
  tags?: string[];
  priority?: SessionPriority;
};

export type WeeklyStressLabel = "low" | "balanced" | "high" | "very_high";

export type WeeklyStressSummary = {
  raw_score: number;
  budget: number;
  relative_load: number;
  label: WeeklyStressLabel;
  display_label: string;
  hard_sessions: number;
  high_fatigue_sessions: number;
  planned_minutes: number;
  estimated_hours: number;
  daily_breakdown: Array<{
    day: string;
    title: string;
    category: SessionCategory;
    score: number;
    label: "low" | "moderate" | "high" | "very_high";
  }>;
  notes: string[];
};

export type WeekContext = {
  program_type: "free_week" | "community_12_week";
  block_number: number | null;
  week_number: number | null;
  block_focus: "sample_week" | "build_the_base" | "build_the_engine" | "build_performance";
  week_focus:
    | "balanced_intro"
    | "base_intro"
    | "base_progression"
    | "base_peak"
    | "base_deload"
    | "engine_intro"
    | "threshold_build"
    | "engine_peak"
    | "engine_deload"
    | "performance_intro"
    | "specificity_peak"
    | "sharpen_and_test"
    | "test_or_taper";
  target_relative_load: number | null;
  target_load_range: { min: number; max: number } | null;
  training_emphasis: {
    aerobic_base: "low" | "moderate" | "high";
    threshold_volume: "low" | "moderate" | "high";
    strength_focus: "movement_quality" | "strength_foundations" | "strength_progression" | "maintenance";
    hybrid_specificity: "low" | "moderate" | "high";
    intensity: "low_moderate" | "moderate" | "moderate_high" | "high";
  } | null;
};

export type StressAlignment = {
  status: "below_target" | "on_target" | "above_target";
  message: string;
};

export type PlanJson = {
  intensity_split: {
    easy_percent: number;
    hard_percent: number;
  };
  weekly_stress?: WeeklyStressSummary;
  week_context?: WeekContext;
  stress_alignment?: StressAlignment | null;
  profile: {
    goal: string;
    training_days: string;
    priority: string;
    level: string;
    weekly_hours: string;
    equipment: string;
    pace_guidance?: PaceGuidance;
  };
  intro: string[];
  schedule: DayPlan[];
  cta: {
    headline: string;
    body: string;
    button_url: string;
  };
};

